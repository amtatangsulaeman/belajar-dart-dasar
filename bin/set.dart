void main(List<String> args) {
  Set<String> fruits = {"Apple", "Orange", "Mango", "Banana"};
  print(fruits);
  print("Fruits is empty : ${fruits.isEmpty} ");
  print("Fruit is Not Empty : ${fruits.isNotEmpty}");
  print("Fruit lengths is : ${fruits.length}");
  print("Fruits first is : ${fruits.first}");
  print("Fruits Last is : ${fruits.last}");
}
